import numpy as np

def ES(losses, alpha=None, VaR=None):

    if VaR is None:
        if alpha is None:
            raise ValueError("Either alpha or VaR must be provided.")
        VaR = np.percentile(losses, 100 * alpha)

    es_value = np.mean(losses[losses > VaR]) if np.any(losses > VaR) else VaR
    return es_value



